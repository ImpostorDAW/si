export function todasLasPalabras() {
  const resultado = [];
  const claves = Object.keys(categorias);
  for (let i = 0; i < claves.length; i++) {
    const palabras = categorias[claves[i]].palabras;
    for (let j = 0; j < palabras.length; j++) {
      resultado.push(palabras[j]);
    }
  }
  return resultado;
}

export function listaCategorias() {
  const resultado = [];
  const claves = Object.keys(categorias);
  for (let i = 0; i < claves.length; i++) {
    resultado.push({
      id: claves[i],
      nombre: categorias[claves[i]].nombre,
    });
  }
  return resultado;
}

export function palabraAleatoria(categoriaId) {
  let palabras;
  if (categoriaId && categorias[categoriaId]) {
    palabras = categorias[categoriaId].palabras;
  } else {
    palabras = todasLasPalabras();
  }

  const indice = Math.floor(Math.random() * palabras.length);
  const seleccionada = palabras[indice];

  let nombreCategoria = null;
  if (categoriaId && categorias[categoriaId]) {
    nombreCategoria = categorias[categoriaId].nombre;
  } else {
    const claves = Object.keys(categorias);
    for (let i = 0; i < claves.length; i++) {
      const cat = categorias[claves[i]];
      for (let j = 0; j < cat.palabras.length; j++) {
        if (cat.palabras[j].palabra === seleccionada.palabra) {
          nombreCategoria = cat.nombre;
          break;
        }
      }
      if (nombreCategoria) break;
    }
  }

  return {
    palabra: seleccionada.palabra,
    pista: seleccionada.pista,
    categoria: nombreCategoria,
  };
}
